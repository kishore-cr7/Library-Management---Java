    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        if (bookname.getText().isEmpty() || isbn.getText().isEmpty() || page.getText().isEmpty() || author.getText().isEmpty() ||quantity.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Enter all details");
    } else {
        try {
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "15-06-2005");
            String query = "UPDATE books SET bookname=?, quantity=?, page=?, author=?,genre=? WHERE isbn=?";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {
                pstmt.setString(1, bookname.getText());
                pstmt.setInt(2, Integer.parseInt(quantity.getText()));
                pstmt.setString(4, author.getText());
                pstmt.setString(5, genre.getSelectedItem().toString());
                pstmt.setInt(3, Integer.parseInt(page.getText()));
                pstmt.setInt(6, Integer.parseInt(isbn.getText()));
                int rowsAffected = pstmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Updated successfully");
                   Selectbook(); 
                } else {
                    JOptionPane.showMessageDialog(null, "No record found with the given isbn");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error updating record: " + e.getMessage());
        } finally {
            try {
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    }//GEN-LAST:event_jButton2MouseClicked

