    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
                               
    if (isbn.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Enter the ISBN.");
    } else {
        try {
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "15-06-2005");
            String id = isbn.getText();
            String query = "DELETE FROM books WHERE isbn=?";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {
                pstmt.setInt(1, Integer.parseInt(isbn.getText()));
                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected == 0) {
                    JOptionPane.showMessageDialog(null, "No book found with the provided ISBN.");
                } else {
                    Selectbook();
                    JOptionPane.showMessageDialog(null, "Product deleted successfully.");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error deleting product: " + e.getMessage());
        } finally {
            try {
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

    }//GEN-LAST:event_jButton3MouseClicked

